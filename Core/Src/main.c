static HAL_StatusTypeDef draw_hello_screen(void){
	HAL_StatusTypeDef status;

	oled_Fill(Black);
	oled_ResetCursor();
	oled_WriteString("Hello!", 6, Font_16x26, White);
	oled_SetCursor(0,40);
	oled_WriteString("press any key", 13, Font_7x10, White);
	oled_SetCursor(0,50);
	oled_WriteString("to continue", 11, Font_7x10, White);

	status = oled_UpdateScreen();
	if (status != HAL_OK) {
		UART_Transmit("Can't draw the hello screen\n");
		return status;
	}

	BUTTON_t btn = BTN_NULL;
	while(btn == BTN_NULL){
		status = keyboard_WaitButton(&btn);
		if (status != HAL_OK) {
			UART_Transmit("Can't wait button after hello screen\n");
			break;
		}
	}
	return status;
}

static HAL_StatusTypeDef read_timer(uint16_t* timer){
	HAL_StatusTypeDef status;
	char time_str = '5';
	while(1){
		oled_Fill(Black);
		oled_ResetCursor();
		oled_WriteString("Time: ", 6, Font_7x10, White);
		oled_WriteString(&time_str, 1, Font_7x10, White);
		oled_WriteString("s", 1, Font_7x10, White);

		status = oled_UpdateScreen();
		if (status != HAL_OK) {
			UART_Transmit("Can't draw time enter screen\n");
			return status;
		}

		BUTTON_t btn = BTN_NULL;
		while(btn == BTN_NULL){
			status = keyboard_WaitButton(&btn);
			if (status != HAL_OK) {
				UART_Transmit("Can't wait a button from keyboard\n");
				return status;
			}
		}

		char c = keyboard_ConvertButtonPhoneCode(btn);
		if (c == '#') break;
		if (c >= '0' && c <= '9') {
			time_str = c;
		}
	}

	*timer = time_str - '0';
	return status;
}

static uint16_t counter;
static HAL_StatusTypeDef draw_counter(void){
	char time_str = '0' + counter;
	oled_Fill(Black);
	oled_ResetCursor();
	oled_WriteString("Remains: ", 9, Font_7x10, White);
	oled_WriteString(&time_str, 1, Font_7x10, White);
	oled_WriteString("s", 1, Font_7x10, White);

	HAL_StatusTypeDef status = oled_UpdateScreen();
	if (status != HAL_OK) {
		UART_Transmit("Can't update a time remaining screen\n");
	}
	return status;
}

static HAL_StatusTypeDef count_down(uint16_t* timer){
	counter = *timer;

	HAL_StatusTypeDef status;
	status = draw_counter();
	if (status != HAL_OK) {
		UART_Transmit("Can't draw a time remaining screen\n");
		return status;
	}

	status = HAL_TIM_Base_Start_IT(&htim6);
	if (status != HAL_OK) {
		UART_Transmit("Can't start count down timer\n");
	}
	return status;
}

static HAL_StatusTypeDef draw_final_screen(void){
	oled_Fill(Black);
	oled_ResetCursor();
	oled_WriteString("You are", 7, Font_16x26, White);
	oled_SetCursor(0,27);
	oled_WriteString("BOOMED!", 7, Font_16x26, White);

	HAL_StatusTypeDef status = oled_UpdateScreen();
	if (status != HAL_OK) {
		UART_Transmit("Can't update final screen\n");
	}
	return status;
}

static void run(void){
	HAL_StatusTypeDef status;

	status = draw_hello_screen();
	if (status != HAL_OK) {
		UART_Transmit("Can't draw hello screen and wait until button\n");
		return;
	}

	uint16_t timer = 0;
	status = read_timer(&timer);
	if (status != HAL_OK) {
		UART_Transmit("Can't read the timer\n");
		return;
	}

	status = count_down(&timer);
	if (status != HAL_OK) {
		UART_Transmit("Can't set count down timer\n");
		return;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if (htim->Instance != TIM6) return;

	draw_counter();
	if (counter-- > 0) return;

	HAL_StatusTypeDef status;
	status = HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
	if (status != HAL_OK) {
		UART_Transmit("Can't start timer 1 channel 1\n");
	}

	status = draw_final_screen();
	if (status != HAL_OK) {
		UART_Transmit("Can't draw the final screen\n");
	}

	HAL_Delay(200);

	status = HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
	if (status != HAL_OK) {
		UART_Transmit("Can't stop timer 1 channel 1\n");
	}

	status = HAL_TIM_Base_Stop_IT(&htim6);
	if (status != HAL_OK) {
		UART_Transmit("Can't stop interrupting timer 6\n");
	}
}
